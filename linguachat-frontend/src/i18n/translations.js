const dictionaries = {
  en: base,
  es: { ...base, ...es },
  pt: { ...base, ...pt },
  fr: { ...base, ...fr },
  it: { ...base, ...it },
  de: { ...base, ...de },
  ja: { ...base, ...ja },
  ar: { ...base, ...ar },
}

export function translate(language, key, params = {}) {
  const template = dictionaries[language]?.[key] || base[key] || key
  return Object.entries(params).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  )
}
